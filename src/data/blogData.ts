export function getPostBySlug(slug: string): BlogPost | undefined {
  return BLOG_POSTS.find((p) => p.slug === slug);
}

/** Newest first (already ordered in BLOG_POSTS; helper for safety). */
export function getPostsSorted(): BlogPost[] {
  return [...BLOG_POSTS].sort((a, b) => (a.date < b.date ? 1 : a.date > b.date ? -1 : 0));
}

/**
 * Related posts for "Read next": prefer shared tags, then recency.
 * Always returns up to `limit` other posts so every article links into the set.
 */
export function getRelatedPosts(slug: string, limit = 3): BlogPost[] {
  const current = getPostBySlug(slug);
  if (!current) return getPostsSorted().slice(0, limit);

  const tagSet = new Set(current.tags.map((t) => t.toLowerCase()));
  const others = BLOG_POSTS.filter((p) => p.slug !== slug);

  const scored = others.map((p) => {
    const shared = p.tags.reduce((n, t) => n + (tagSet.has(t.toLowerCase()) ? 1 : 0), 0);
    return { post: p, shared, date: p.date };
  });

  scored.sort((a, b) => {
    if (b.shared !== a.shared) return b.shared - a.shared;
    if (a.date < b.date) return 1;
    if (a.date > b.date) return -1;
    return 0;
  });

  return scored.slice(0, limit).map((s) => s.post);
}
